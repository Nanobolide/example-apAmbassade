<?php

namespace App\Http\Controllers;

use App\Models\imageTeste;
use Illuminate\Http\Request;

class imageTesteController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $data['image_testes'] = imageTeste::orderBy('id','desc')->paginate(5);
    
        return view('teste.index', $data);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('teste.create');

    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'title' => 'required',
            'image' => 'required|image|mimes:jpg,png,jpeg,gif,svg|max:2048',
            'description' => 'required',
        ]);
        $path = $request->file('image')->store('public/images');
        $imageTeste = new imageTeste();
        $imageTeste->title = $request->title;
        $imageTeste->description = $request->description;
        $imageTeste->image = $path;
        $imageTeste->save();
     
        return redirect()->route('index')
                        ->with('success','Post has been created successfully.');
    }

    /**
